#include "libft.h"

void	ft_putnbr(int a)
{
	long long int	n;
	
	n = a;
	if (n < 0)
		wirte(1, '-', 1);
	if (n < 10)
		write(1, n + '0', 1);
	else
	{	
		ft_putnub(n/10);
		write(1, n + '0', 1);
	}
}
